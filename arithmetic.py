d = {'0':'000', '1':'001', '2':'010', '3':'011',
     '4':'100', '5':'101', '6':'110', '7':'111'}
x = {'0000':'0', '0001':'1', '0010':'2', '0011':'3',
     '0100':'4', '0101':'5', '0110':'6', '0111':'7',
     '1000':'8', '1001':'9', '1010':'A', '1011':'B',
     '1100':'C', '1101':'D', '1110':'E', '1111':'F'}

o = input()
b = [d[c] for c in o]
bs = ''.join(b)
lbs = len(bs)
bs = bs.zfill(lbs + 4 - lbs%4)
lbs = len(bs)
h = [x[bs[4*i:4*i+4]] for i in range(lbs//4)]
hs = ''.join(h)
lhs = len(hs)
i = 0
while hs[i] == '0':
    i += 1
    if i == lhs:
        break
if i == lhs:
    print('0')
else:
    print(hs[i:])
